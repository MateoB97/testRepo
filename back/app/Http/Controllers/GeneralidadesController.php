     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $index = Generalidades::all()->first();

        if ($index == null) {
            $nuevoItem = new Generalidades($request->all());
            $nuevoItem->save();
        } else {
            $index->prod_cuarto_dela = $request->prod_cuarto_dela;
            $index->prod_cuarto_tras = $request->prod_cuarto_tras;
            $index->prod_canal = $request->prod_canal;
            $index->save();
        }
        

        return 'done';
    }

